package net.dakotapride.garnishedstoneautomation.extractor;

import com.jozufozu.flywheel.api.Instancer;
import com.jozufozu.flywheel.api.MaterialManager;
import com.simibubi.create.AllPartialModels;
import com.simibubi.create.content.kinetics.base.SingleRotatingInstance;
import com.simibubi.create.content.kinetics.base.flwdata.RotatingData;

public class MechanicalExtractorCogInstance extends SingleRotatingInstance<MechanicalExtractorBlockEntity> {

    public MechanicalExtractorCogInstance(MaterialManager materialManager, MechanicalExtractorBlockEntity blockEntity) {
        super(materialManager, blockEntity);
    }

    @Override
    protected Instancer<RotatingData> getModel() {
        return getRotatingMaterial().getModel(AllPartialModels.SHAFTLESS_COGWHEEL, blockEntity.getBlockState());
    }
}
